package operadoresAritmeticosChars;

public class OperacionesBasicasChars {
    public static void main(String[] args) {
        char c1 = 'E', c2 = '5', c3 = '?';
        System.out.println("c1 + 1 = " + (c1 + 1));
        System.out.println("c2 - 2 = " + (c2 - 2));
        System.out.println("c2 - '2' = " + (c2 - '2'));
        System.out.println("c3 + '#' = " + (c3 + '#'));
    }
}