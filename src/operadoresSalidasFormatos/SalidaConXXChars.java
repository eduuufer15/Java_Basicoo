package operadoresSalidasFormatos;

public class SalidaConXXChars {
    public static void main(String[] args) {
        System.out.println("c1 + 1 = 70");
        System.out.println("c1 - c2 + c3 = XX");
        System.out.println("c2 - 2 = XX");
        System.out.println("c2 - '2' = XX");
    }
}