package operadoresSalidasFormatos;

public class MostrarCaracteresAscii {
    public static void main(String[] args) {
        char c = 69; // E
        System.out.println("Caracter correspondiente a 69 = " + c);
        c = 53; // 5
        System.out.println("Caracter correspondiente a 53 = " + c);
        c = 63; // ?
        System.out.println("Caracter correspondiente a 63 = " + c);
    }
}